/**
 * Generates lib/build-info.generated.ts at build/dev time.
 *
 * On Vercel, commit metadata comes from VERCEL_GIT_COMMIT_SHA automatically.
 * Locally, falls back to `git rev-parse --short HEAD`.
 */
const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const packageJson = require('../package.json')

function getCommitShort() {
  const vercelSha = process.env.VERCEL_GIT_COMMIT_SHA?.trim()
  if (vercelSha) return vercelSha.slice(0, 7)

  try {
    return execSync('git rev-parse --short=7 HEAD', { encoding: 'utf8' }).trim()
  } catch {
    return 'local'
  }
}

function resolveAppEnv() {
  const explicit = process.env.NEXT_PUBLIC_APP_ENV?.toLowerCase()
  if (explicit === 'development' || explicit === 'staging' || explicit === 'production') {
    return explicit
  }

  switch (process.env.VERCEL_ENV) {
    case 'production':
      return 'production'
    case 'preview':
      return 'staging'
    case 'development':
      return 'development'
  }

  return process.env.NODE_ENV === 'production' ? 'production' : 'development'
}

function toDeployLabel(appEnv) {
  switch (appEnv) {
    case 'production':
      return 'PROD'
    case 'staging':
      return 'PREVIEW'
    default:
      return 'DEV'
  }
}

const appEnv = resolveAppEnv()
const buildInfo = {
  version: packageJson.version,
  commitSha: getCommitShort(),
  buildTime: new Date().toISOString(),
  appEnv,
  deployLabel: toDeployLabel(appEnv),
}

const outPath = path.join(__dirname, '../lib/build-info.generated.ts')
const fileContents = `// AUTO-GENERATED by scripts/generate-build-info.js — do not edit manually.
export const BUILD_INFO = ${JSON.stringify(buildInfo, null, 2)} as const
`

fs.writeFileSync(outPath, fileContents, 'utf8')

console.log(
  `[build-info] v${buildInfo.version} · ${buildInfo.commitSha} · ${buildInfo.deployLabel} · ${buildInfo.buildTime}`,
)
